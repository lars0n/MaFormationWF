/* ----------------------------------
    INCREMENTATION & DECREMENTATION
------------------------------------ */

var nb1 = 1;

// ############ Incrémentation ######## //

nb1 = nb1 + 1;

// -- Affichage dans la console
console.log(nb1);

// -- Ecriture Simplifié
nb1++;

// -- Affichage dans la console
console.log(nb1);

// ############ Décrémentation ######## //

nb1 = nb1 - 1;

// -- Affichage dans la console
console.log(nb1);

// -- Ecriture Simplifié
nb1--;

// -- Affichage dans la console
console.log(nb1);

// ############ Subtilité ######## //

nb1 = 1;
console.log("nb1++ = " + nb1++);
console.log("nb1++ = " + ++nb1);